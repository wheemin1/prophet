export function getTemplatesByPeriod(period: Period): string[] {
  switch (period) {
    case "daily":
      return dailyTemplates;
    case "weekly":
      return weeklyTemplates;
    case "monthly":
      return monthlyTemplates;
    case "yearly":
      return yearlyTemplates;
    default:
      return dailyTemplates;
  }
}
